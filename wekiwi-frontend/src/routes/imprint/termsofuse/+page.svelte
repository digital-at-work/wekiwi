    <li>Datenschutzverletzungen melden wir unverzüglich, spätestens innerhalb von 48 Stunden.</li>
    <li>Nach Vertragsende löschen wir sämtliche Nutzerdaten. Backups werden automatisiert nach Ablauf der Retention-Zeiten gelöscht.</li>
