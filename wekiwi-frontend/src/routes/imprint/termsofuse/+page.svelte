        <li><strong>Hetzner Online GmbH</strong> – App-Hosting, ISO 27001-zertifiziert</li>
        <li><strong>EClabs</strong> – administrative Betreuung der Applikation bei Hetzner</li>
      </ul>
    </li>
    <li>Technisch-organisatorische Maßnahmen nach „Stand der Technik“ (z. B. TLS, Zugangskontrollen, Backups).</li>
