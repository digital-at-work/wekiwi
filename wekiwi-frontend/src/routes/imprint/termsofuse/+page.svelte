  <p>we:kiwi ist eine Open‑Source‑Wissensplattform zur Texterzeugung, betrieben unter der AGPL‑Lizenz. Gehostet in Deutschland:</p>
  <ul>
    <li>Virtual Machines von <strong>Centron GmbH</strong>, administriert durch das we:kiwi-Team</li>
