	<h3 class="wp-block-heading">3.5 Implizites Feedback und Interaktionen</h3>
	<p>
		Eine der Kernfunktionen von we:kiwi ist es, zu erkennen, welche Beiträge für individuelle
		Nutzer*innen relevant sind, um diese bevorzugt anzuzeigen. Dies gelingt nur über sog. <em
			>implizites Feedback,</em
		> d.h. Interaktionen.
	</p>
	<p>
		Jedes Mal, wenn Du einen Beitrag erstellst, kommentierst, betrachtest oder eine Reaktion
		hinterlässt, lernt unser <strong>Algorithmus</strong>. Damit der Algorithmus mit einer
		Interaktion etwas anfangen kann, mussten wir uns ein System ausdenken, das in Zahlen ausdrückt,
		was eine Interaktion über die Relevanz der Karte für eine Nutzer:in aussagt. Dazu wird für
		Interaktionen ein <strong>Score</strong> vergeben, also eine Punktzahl.
	</p>
	<p>Das Scoring-System vergibt Punkte nach folgendem Schema:</p>
	<figure class="wp-block-table">
		<table>
			<tbody
				><tr><td>Eine Karte erstellen</td><td>1 Punkt</td></tr><tr
					><td
						>Lösungsvorschlag zu einer Challenge-Karte machen oder einen Lösungsvorschlag als Lösung
						markieren</td
					><td>0,8 Punkte</td></tr
				><tr><td>Eine Karte pinnen</td><td>0,5 Punkte</td></tr><tr
					><td>Eine Karte kommentieren</td><td>0,3 Punkte</td></tr
				><tr
					><td>Auf eine Karte mit einem positiven Emoji oder einem Upvote reagieren</td><td
						>0,2 Punkte</td
					></tr
				><tr><td>Eine Karte näher ansehen</td><td>0,1 Punkt</td></tr><tr
					><td>Auf eine Karte mit einem negativen Emoji reagieren</td><td>-1 Punkt</td></tr
				></tbody
			>
		</table>
	</figure>
	<p>
		Eine Karte, die hoch punktet, wird als relevanter für die Nutzer:in angesehen als eine Karte,
		mit der nur wenig oder überhaupt nicht interagiert wurde.
	</p>
	<p>
		Ausgehend von dieser Annahme werden die Ratings dazu verwendet, ein <em
			>Deep Learning Recommender Model</em
		> zu trainieren, welches dann Vorhersagen über neue Nutzer-/Kartenpaare trifft. Karten, welche die
		trainierte KI aufgrund ihrer Eigenschaften für besonders relevant hält, werden der Nutzer:in bevorzugt
		angezeigt,
